# Append: Adds A New Element To The End Of A List

a = ['w','x','y']
a.append('z')
print(a)

# Extend: Takes A List  As An Argument And Appends All Of The Elements

a1 = ['v','w','x']
a2 = ['y','z']
a1.extend(a2)
print(a1)

# Sort: Arranges The Element Of The List From Low o High

a = ['z','x','w','y']
a.sort()
print(a)
