s = "My Own String"

print(s.capitalize())       # Upper-casing first letter

print(s.count("n"))         # Count of a particular character in string

print(s.endswith("ing"))    # Check if a string ends with a pattern

print(s.startswith("My"))   # Check if a string starts with a pattern

print(s.isalpha())          # Returns True if "all" characters in the string are in the alphabet

print(s.isdigit())          # Returns True if "all" characters in the string are numbers

print(s.isspace())          # Returns True if "all" characters in the string are whitespace

print(s.strip())            # Removes Leading and Trailing whitespaces

print(s.upper())            # Converting a string to Upper Case

print(s.lower())            # Converting a string to Lower Case/Down Case

print(s.isupper())          # Checks if all chars are in UpperCase

print(s.islower())          # Checks if all chars are in LowerCase

print(s.index("O"))         # Returns index of given argument
